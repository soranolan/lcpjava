package com.example.lcpjava.common;

public class Lcp_101_TreeNode {
	
	public int val;
	
	public Lcp_101_TreeNode left;
	
	public Lcp_101_TreeNode right;
	
	public Lcp_101_TreeNode() {  }
	
	public Lcp_101_TreeNode(int val) {
		this.val = val;
	}
	
	public Lcp_101_TreeNode(int val, Lcp_101_TreeNode left, Lcp_101_TreeNode right) {
		this.val = val;
		this.left = left;
		this.right = right;
	}
	
}
